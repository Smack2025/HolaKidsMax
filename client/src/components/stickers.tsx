export interface StickerProps {
  type: keyof typeof STICKER_COLLECTION;
  size?: 'sm' | 'md' | 'lg';
  className?: string;
}

export function Sticker({ type, size = 'md', className = '' }: StickerProps) {
  const sizeClasses = {
    sm: 'w-12 h-12',
    md: 'w-16 h-16', 
    lg: 'w-24 h-24'
  };

  return (
    <div 
      className={`${sizeClasses[size]} ${className}`}
      dangerouslySetInnerHTML={{ __html: STICKER_COLLECTION[type] }}
    />
  );
}

